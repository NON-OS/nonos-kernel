pub fn lookup_static(index: usize) -> Option<(&'static str, &'static str)> {
    STATIC_TABLE.get(index.wrapping_sub(1)).copied()
}

pub fn find_static(name: &str, value: &str) -> Option<usize> {
    for (i, &(n, v)) in STATIC_TABLE.iter().enumerate() {
        if n == name && v == value {
            return Some(i + 1);
        }
    }
    None
}

pub fn find_static_name(name: &str) -> Option<usize> {
    for (i, &(n, _)) in STATIC_TABLE.iter().enumerate() {
        if n == name {
            return Some(i + 1);
        }
    }
    None
}

