pub fn hid_to_scancode(hid_usage: u8) -> u8 {
    match hid_usage {
        0x04..=0x1D => {
            const MAP: [u8; 26] = [
                0x1E, 0x30, 0x2E, 0x20, 0x12, 0x21, 0x22, 0x23, 0x17, 0x24,
                0x25, 0x26, 0x32, 0x31, 0x18, 0x19, 0x10, 0x13, 0x1F, 0x14,
                0x16, 0x2F, 0x11, 0x2D, 0x15, 0x2C,
            ];
            MAP[(hid_usage - 0x04) as usize]
        }
        0x1E..=0x27 => {
            const MAP: [u8; 10] = [0x02, 0x03, 0x04, 0x05, 0x06, 0x07, 0x08, 0x09, 0x0A, 0x0B];
            MAP[(hid_usage - 0x1E) as usize]
        }
        0x28 => 0x1C, 0x29 => 0x01, 0x2A => 0x0E, 0x2B => 0x0F, 0x2C => 0x39,
        0x2D => 0x0C, 0x2E => 0x0D, 0x2F => 0x1A, 0x30 => 0x1B, 0x31 => 0x2B,
        0x33 => 0x27, 0x34 => 0x28, 0x35 => 0x29, 0x36 => 0x33, 0x37 => 0x34,
        0x38 => 0x35, 0x39 => 0x3A,
        0x3A..=0x45 => 0x3B + (hid_usage - 0x3A),
        0x49 => 0x52, 0x4A => 0x47, 0x4B => 0x49, 0x4C => 0x53, 0x4D => 0x4F,
        0x4E => 0x51, 0x4F => 0x4D, 0x50 => 0x4B, 0x51 => 0x50, 0x52 => 0x48,
        0x53 => 0x45, 0x54 => 0x35, 0x55 => 0x37, 0x56 => 0x4A, 0x57 => 0x4E,
        0x58 => 0x1C,
        0x59..=0x62 => {
            const MAP: [u8; 10] = [0x4F, 0x50, 0x51, 0x4B, 0x4C, 0x4D, 0x47, 0x48, 0x49, 0x52];
            MAP[(hid_usage - 0x59) as usize]
        }
        0x63 => 0x53,
        0xE0 => 0x1D, 0xE1 => 0x2A, 0xE2 => 0x38, 0xE3 => 0x5B,
        0xE4 => 0x1D, 0xE5 => 0x36, 0xE6 => 0x38, 0xE7 => 0x5C,
        _ => 0,
    }
}
