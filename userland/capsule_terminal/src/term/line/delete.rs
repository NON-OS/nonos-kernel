use super::types::Line;

impl Line {
    pub fn delete(&mut self) -> bool {
        if self.cursor >= self.len {
            return false;
        }
        self.buf.copy_within(self.cursor + 1..self.len, self.cursor);
        self.len -= 1;
        true
    }
}
